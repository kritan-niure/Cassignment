#include <stdio.h>
int main()
{  
  	for(int n=100; n<=500; n++)
  {
  		int r, rev=0, temp=n;
  	    while(temp>0)
            { 
      	       	r=temp%10;
      		    rev=rev*10+r;
      		    temp=temp/10;
      	   }
            	if(rev==n)
                {
          	    	printf("%d\t",n);
              	}
      }
  	return 0;
}
