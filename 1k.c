#include <stdio.h>
int main()
{
  	int a,count=0;
  	scanf("%d", &a);
  	for(int i=2; i<a; i++)
     {
    		if(a%i==0)
        {
      			count++;
     
      		}
  	  }
  	if(count==0)
       {
    		printf("It is a prime number.");
  	  }
  	else
       {
  		  printf("It is not a prime number.");
    	}
  	return 0;
 }
